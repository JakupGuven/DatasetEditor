package de;

public class Class {
	private String className;
	private int oldClassIndex;
	private int newClassIndex = -1;
	
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public int getOldClassIndex() {
		return oldClassIndex;
	}
	public void setOldClassIndex(int oldClassIndex) {
		this.oldClassIndex = oldClassIndex;
	}
	public int getNewClassIndex() {
		return newClassIndex;
	}
	public void setNewClassIndex(int newClassIndex) {
		this.newClassIndex = newClassIndex;
	}



}
